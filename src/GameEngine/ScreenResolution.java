package GameEngine;

/**
 *
 * @author Mohamed
 */
public class ScreenResolution {
    
    int width;
    int height;
    
    public ScreenResolution(int width, int height){
        
        this.width = width;
        this.height = height;
        
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
    
}
